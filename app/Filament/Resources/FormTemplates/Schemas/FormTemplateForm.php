<?php

namespace App\Filament\Resources\FormTemplates\Schemas;

use App\Models\Branch;
use App\Models\FormTemplate;
use App\Models\User;
use Filament\Forms\Components\Placeholder;
use Filament\Forms\Components\Select;
use Filament\Forms\Components\TextInput;
use Filament\Forms\Components\Toggle;
use Filament\Infolists\Components\TextEntry;
use Filament\Schemas\Components\Utilities\Get;
use Filament\Schemas\Components\Utilities\Set;
use Filament\Schemas\Schema;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Validation\Rule;

class FormTemplateForm
{
    public static function configure(Schema $schema): Schema
    {
        return $schema
            ->components([
                Select::make('branch_id')
                    ->label('Select Branch')
                    ->relationship('branch', 'name')
                    ->searchable()
                    ->preload()
                    ->live()
                    ->afterStateUpdated(fn (Set $set) => $set('user_id', null))
                    ->required()
                    ->rules([
                        // On CREATE: branch_id must not already exist in form_templates
                        fn (Get $get) => Rule::unique(FormTemplate::class, 'branch_id')
                            ->ignore(
                                // On EDIT: ignore the current record's own branch_id
                                request()->route('record')
                            ),
                    ])
                    ->validationMessages([
                        'unique' => 'A form template already exists for this branch. Each branch can only have one template.',
                    ])
                    ->placeholder('Select a branch'),
                // TextInput::make('active_version_id')
                //     ->numeric(),
                Select::make('user_id')
                    ->label('Select User')
                    ->options(function (Get $get): array {
                        $branchId = $get('branch_id');

                        if (! $branchId) {
                            return [];
                        }

                        return User::whereHas('branch', function ($query) use ($branchId) {
                                $query->where('branches.id', $branchId);
                            })
                            ->pluck('name', 'id')
                            ->toArray();
                    })
                    ->searchable()
                    ->preload()
                    ->required()
                    ->placeholder('Select a user'),

                TextInput::make('registration_serial')
                    ->required(),
                TextInput::make('name')
                    ->required(),
                TextInput::make('slug')
                    ->required(),
                Select::make('type')
                    ->options(['admission' => 'Admission'])
                    ->default('admission')
                    ->required(),
                Select::make('status')
                    ->options(['draft' => 'Draft', 'published' => 'Published', 'archived' => 'Archived'])
                    ->default('draft')
                    ->required(),
                TextInput::make('form_layout')
                    ->required()
                    ->default('default'),
                Select::make('rollno_generation_scope')
                    ->label('Roll Number Generated By')
                    ->options(['class' => 'Class', 'section' => 'Section'])
                    ->default('section')
                    ->required(),
                Toggle::make('is_active')
                    ->required()
                    ->columnSpanFull(),
            ]);
    }
}
